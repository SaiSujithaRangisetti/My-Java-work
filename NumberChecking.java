package Basics;

public class NumberChecking {
    public static void main(String[] args) {
        int a=10;
        if(a==10){
            System.out.println("True a==10");
        }
        else if(a<10){
            System.out.println("True a<10");
        }
        else if (a>10){
            System.out.println("True a>10");
        }
        else if(a!=0){
            System.out.println("True a!=10");
        }
    }
}
