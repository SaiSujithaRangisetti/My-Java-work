package Basics;

public class RelationalOperators {
    public static void main(String[] args){
        int a=100;
        int b=403;
        System.out.println(a>b);
        System.out.println(a<b);
        System.out.println(a>=b);
        System.out.println(a<=b);
        System.out.println(a==b);
    }
}
