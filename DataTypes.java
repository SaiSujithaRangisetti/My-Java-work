package Basics;

public class DataTypes {
    public static void main(String[] args){
        int b=10;
        float a=10.5f;
        System.out.println(a/b);
        double c=100.405;
        byte d=126;
        System.out.println(c+d);
        long e=10350989;
        short f=63;
        System.out.println(e-f);
        int g=(10+20)*(10-5);
        int h=(510*700)-(10*60);
        System.out.println(g+h);
    }
}
